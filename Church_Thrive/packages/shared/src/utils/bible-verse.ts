export interface BibleVerseRef {
  book: string;
  fullBook: string;
  chapter: number;
  verseStart: number;
  verseEnd: number | null;
  raw: string;
}

export function detectBibleVerses(text: string): BibleVerseRef[] {
  const matches = text.match(VERSE_PATTERN);
  if (!matches) return [];

  return matches.map((raw) => {
    const parts = raw.match(/^(.+?)\s*(\d+)\s*:\s*(\d+)(?:\s*[-~]\s*(\d+))?$/);
    if (!parts) return null;

    const [, bookAbbr, chapter, verseStart, verseEnd] = parts;
    const trimmedBook = bookAbbr.trim();
    const fullBook = BIBLE_BOOKS_KO[trimmedBook] || trimmedBook;

    return {
      book: trimmedBook,
      fullBook,
      chapter: parseInt(chapter, 10),
      verseStart: parseInt(verseStart, 10),
      verseEnd: verseEnd ? parseInt(verseEnd, 10) : null,
      raw,
    };
  }).filter(Boolean) as BibleVerseRef[];
}

export function formatVerseRef(ref: BibleVerseRef): string {
  const verse = ref.verseEnd
    ? `${ref.verseStart}-${ref.verseEnd}`
    : `${ref.verseStart}`;
  return `${ref.fullBook} ${ref.chapter}:${verse}`;
}
